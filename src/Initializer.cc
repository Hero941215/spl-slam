//点线特征的单目构造函数
Initializer::Initializer(const Frame &ReferenceFrame, float sigma, int iterations, const bool usingLine):mbusingLine(usingLine)
{
    mK = ReferenceFrame.mK.clone();

    mvKeys1 = ReferenceFrame.mvKeysUn;

    if(mbusingLine)
    {
      mvKeyMidP1 = ReferenceFrame.mvMidPointsUn;
      mvLinesUn1 = ReferenceFrame.mvLinesUn;
    }
    
    mSigma = sigma;
    mSigma2 = sigma*sigma;
    mMaxIterations = iterations;
}

bool Initializer::Initialize(const Frame &CurrentFrame, const vector<int> &vMatches12, cv::Mat &R21, cv::Mat &t21,
                             vector<cv::Point3f> &vP3D, vector<bool> &vbTriangulated)
{
    // Fill structures with current keypoints and matches with reference frame
    // Reference Frame: 1, Current Frame: 2
    mvKeys2 = CurrentFrame.mvKeysUn;

    mvMatches12.clear();
    mvMatches12.reserve(mvKeys2.size());
    mvbMatched1.resize(mvKeys1.size());
    for(size_t i=0, iend=vMatches12.size();i<iend; i++)
    {
        if(vMatches12[i]>=0)
        {
            mvMatches12.push_back(make_pair(i,vMatches12[i]));
            mvbMatched1[i]=true;
        }
        else
            mvbMatched1[i]=false;
    }

    const int N = mvMatches12.size();

    // Indices for minimum set selection
    vector<size_t> vAllIndices;
    vAllIndices.reserve(N);
    vector<size_t> vAvailableIndices;

    for(int i=0; i<N; i++)
    {
        vAllIndices.push_back(i);
    }

    // Generate sets of 8 points for each RANSAC iteration
    mvSets = vector< vector<size_t> >(mMaxIterations,vector<size_t>(8,0));

    DUtils::Random::SeedRandOnce(0);

    for(int it=0; it<mMaxIterations; it++)
    {
        vAvailableIndices = vAllIndices;

        // Select a minimum set
        for(size_t j=0; j<8; j++)
        {
            int randi = DUtils::Random::RandomInt(0,vAvailableIndices.size()-1);
            int idx = vAvailableIndices[randi];

            mvSets[it][j] = idx;

            vAvailableIndices[randi] = vAvailableIndices.back();
            vAvailableIndices.pop_back();
        }
    }

    // Launch threads to compute in parallel a fundamental matrix and a homography
    vector<bool> vbMatchesInliersH, vbMatchesInliersF;
    float SH, SF;
    cv::Mat H, F;

    thread threadH(&Initializer::FindHomography,this,ref(vbMatchesInliersH), ref(SH), ref(H));
    thread threadF(&Initializer::FindFundamental,this,ref(vbMatchesInliersF), ref(SF), ref(F));

    // Wait until both threads have finished
    threadH.join();
    threadF.join();

    // Compute ratio of scores
    float RH = SH/(SH+SF);

    // Try to reconstruct from homography or fundamental depending on the ratio (0.40-0.45)
    if(RH>0.40)
        return ReconstructH(vbMatchesInliersH,H,mK,R21,t21,vP3D,vbTriangulated,1.0,50);
    else //if(pF_HF>0.6)
        return ReconstructF(vbMatchesInliersF,F,mK,R21,t21,vP3D,vbTriangulated,1.0,50);

    return false;
}

//点线特征初始化主函数：
bool Initializer::InitializeBoth(const Frame &CurrentFrame, const vector<int> &vMatches12, const vector<int> &vMatchesLines12, cv::Mat &R21, cv::Mat &t21,
		   vector<cv::Point3f> &vP3D, vector<cv::Point3f> &vMidP3D ,vector<cv::Point3f> &vFirP3D ,vector<cv::Point3f> &vEndP3D ,
		   vector<bool> &vbTriangulated, vector<bool> &vbTriangulatedMidP)
{
    // Reference Frame: 1, Current Frame: 2
    mvKeys2 = CurrentFrame.mvKeysUn;
    mvKeyMidP2 = CurrentFrame.mvMidPointsUn;
    mvLinesUn2 = CurrentFrame.mvLinesUn;
    
    //点匹配记录
    mvMatches12.clear();
    mvMatches12.reserve(mvKeys2.size());
    mvbMatched1.resize(mvKeys1.size());//对应初始图像帧的关键点提取数量
    for(size_t i=0, iend=vMatches12.size();i<iend; i++)
    {
        if(vMatches12[i]>=0)
        {
            mvMatches12.push_back(make_pair(i,vMatches12[i]));
            mvbMatched1[i]=true;
        }
        else
            mvbMatched1[i]=false;
    }
    //线匹配记录
    mvMatchesLines12.clear();
    mvMatchesLines12.reserve(mvLinesUn2.size());
    mvbMatchedLines1.resize(mvLinesUn1.size());
    for(size_t i=0, iend=vMatchesLines12.size();i<iend; i++)
    {
        if(vMatchesLines12[i]>=0)
        {
            mvMatchesLines12.push_back(make_pair(i,vMatchesLines12[i]));
            mvbMatchedLines1[i]=true;
        }
        else
            mvbMatchedLines1[i]=false;
    }
    
    //创建一个点线特征统一的的索引容器vAllIndices，用于存储0到N+NL
    const int N = mvMatches12.size();
    const int NL = mvMatchesLines12.size();
    
    // Indices for minimum set selection
    vector<size_t> vAllIndices;
    vAllIndices.reserve(N+NL);
    vector<size_t> vAvailableIndices;

    for(int i=0; i<N+NL; i++)
    {
        vAllIndices.push_back(i);
    }

    // Generate sets of 8 points for each RANSAC iteration
    mvSets = vector< vector<size_t> >(mMaxIterations,vector<size_t>(8,0));

    DUtils::Random::SeedRandOnce(0);

    for(int it=0; it<mMaxIterations; it++)
    {
        vAvailableIndices = vAllIndices;

        // Select a minimum set
        for(size_t j=0; j<8; j++)
        {
            int randi = DUtils::Random::RandomInt(0,vAvailableIndices.size()-1);
            int idx = vAvailableIndices[randi];

            mvSets[it][j] = idx;

            vAvailableIndices[randi] = vAvailableIndices.back();
            vAvailableIndices.pop_back();
        }
    }
   
    // Launch threads to compute in parallel a fundamental matrix and a homography
    vector<bool> vbMatchesInliersH, vbMatchesInliersF;
    vector<bool> vbMatchesInlierLinesH, vbMatchesInlierLinesF;
    float SH, SF;
    cv::Mat H, F;

    thread threadH(&Initializer::FindHomographyBoth,this,ref(vbMatchesInliersH), ref(vbMatchesInlierLinesH), ref(SH), ref(H));
    thread threadF(&Initializer::FindFundamentalBoth,this,ref(vbMatchesInliersF), ref(vbMatchesInlierLinesF), ref(SF), ref(F));

    // Wait until both threads have finished
    threadH.join();
    threadF.join();

    // Compute ratio of scores
    float RH = SH/(SH+SF);
    
    cout << "分线程初始化数据计算H和F矩阵，得分比例为: " << RH << endl << endl; 

    // Try to reconstruct from homography or fundamental depending on the ratio (0.40-0.45)
    if(RH>0.40)
    {
	bool RHPoints;
	bool RHLines;
	thread threadReconstructHPoints(&Initializer::ReconstructHPoints,this, ref(vbMatchesInliersH),
					ref(H),ref(mK),ref(R21),ref(t21),ref(vP3D),ref(vbTriangulated),1.0,50,ref(RHPoints));
	thread threadReconstructHLines(&Initializer::ReconstructHLines,this, ref(vbMatchesInlierLinesH),
					ref(H),ref(mK),ref(R21),ref(t21),ref(vMidP3D),ref(vFirP3D),ref(vEndP3D),ref(vbTriangulatedMidP),1.0,50,ref(RHLines));
	
	threadReconstructHPoints.join();
	threadReconstructHLines.join();
		
	return RHPoints && RHLines;
    }
        
    else //if(pF_HF>0.6)
    {
	bool RFPoints;
	bool RFLines;
	thread threadReconstructFPoints(&Initializer::ReconstructFPoints,this, ref(vbMatchesInliersF),
					ref(F),ref(mK),ref(R21),ref(t21),ref(vP3D),ref(vbTriangulated),1.0,50,ref(RFPoints));
	thread threadReconstructFLines(&Initializer::ReconstructFLines,this, ref(vbMatchesInlierLinesF),
					ref(F),ref(mK),ref(R21),ref(t21),ref(vMidP3D),ref(vFirP3D),ref(vEndP3D),ref(vbTriangulatedMidP),1.0,50,ref(RFLines));
	
	threadReconstructFPoints.join();
	threadReconstructFLines.join();
	
	return RFPoints && RFLines;
    }
        

    return false;
}

void Initializer::FindHomography(vector<bool> &vbMatchesInliers, float &score, cv::Mat &H21)
{
    // Number of putative matches
    const int N = mvMatches12.size();

    // Normalize coordinates
    vector<cv::Point2f> vPn1, vPn2;
    cv::Mat T1, T2;
    Normalize(mvKeys1,vPn1, T1);
    Normalize(mvKeys2,vPn2, T2);
    cv::Mat T2inv = T2.inv();

    // Best Results variables
    score = 0.0;
    vbMatchesInliers = vector<bool>(N,false);

    // Iteration variables
    vector<cv::Point2f> vPn1i(8);
    vector<cv::Point2f> vPn2i(8);
    cv::Mat H21i, H12i;
    vector<bool> vbCurrentInliers(N,false);
    float currentScore;

    // Perform all RANSAC iterations and save the solution with highest score
    for(int it=0; it<mMaxIterations; it++)
    {
        // Select a minimum set
        for(size_t j=0; j<8; j++)
        {
            int idx = mvSets[it][j];

            vPn1i[j] = vPn1[mvMatches12[idx].first];
            vPn2i[j] = vPn2[mvMatches12[idx].second];
        }

        cv::Mat Hn = ComputeH21(vPn1i,vPn2i);
        H21i = T2inv*Hn*T1;
        H12i = H21i.inv();

        currentScore = CheckHomography(H21i, H12i, vbCurrentInliers, mSigma);

        if(currentScore>score)
        {
            H21 = H21i.clone();
            vbMatchesInliers = vbCurrentInliers;
            score = currentScore;
        }
    }
}

//基于匹配的点特征和线特征中点计算H矩阵及其得分
void Initializer::FindHomographyBoth(vector<bool> &vbMatchesInliers, vector<bool> &vbMatchesInlierLines, float &score, cv::Mat &H21)
{
    // Number of putative matches
    const int N = mvMatches12.size();
    const int NL = mvMatchesLines12.size();
    
    // Normalize coordinates
    vector<cv::Point2f> vPn1, vPn2;
    vector<cv::Point2f> vMidPn1, vMidPn2;
    
    //归一化矩阵
    cv::Mat T1, T2;
    NormalizeBoth(mvKeys1, mvKeyMidP1, vPn1, vMidPn1, T1);
    NormalizeBoth(mvKeys2, mvKeyMidP2, vPn2, vMidPn2, T2);
    cv::Mat T2inv = T2.inv();

    // Best Results variables
    score = 0.0;
    vbMatchesInliers = vector<bool>(N,false);
    vbMatchesInlierLines = vector<bool>(NL,false);
    
    // Iteration variables
    vector<cv::Point2f> vPn1i(8);
    vector<cv::Point2f> vPn2i(8);
    cv::Mat H21i, H12i;

    vector<bool> vbCurrentInliers(N,false);
    float currentScore;
    vector<bool> vbCurrentInliersLines(NL,false);
    float currentScoreLines;

    // Perform all RANSAC iterations and save the solution with highest score
    for(int it=0; it<mMaxIterations; it++)
    {
        // Select a minimum set
        for(size_t j=0; j<8; j++)
        {
            int idx = mvSets[it][j];
	    
	    //根据idx大小判断其为点匹配还是线匹配
	    if(idx<N)
	    {
	      vPn1i[j] = vPn1[mvMatches12[idx].first];
	      vPn2i[j] = vPn2[mvMatches12[idx].second];
	    }
	    else
	    {
	      vPn1i[j] = vMidPn1[mvMatchesLines12[idx-N].first];
	      vPn2i[j] = vMidPn2[mvMatchesLines12[idx-N].second];
	    }
            
        }

        cv::Mat Hn = ComputeH21(vPn1i,vPn2i);//使用ORB-SLAM2中的自带函数
        H21i = T2inv*Hn*T1;
        H12i = H21i.inv();

        currentScore = CheckHomography(H21i, H12i, vbCurrentInliers, mSigma);
	currentScoreLines = CheckHomographyLines(H21i, H12i, vbCurrentInliersLines, mSigma);
	
        if(currentScore+currentScoreLines>score)
        {
            H21 = H21i.clone();
            vbMatchesInliers = vbCurrentInliers;
	    vbMatchesInlierLines = vbCurrentInliersLines;
            score = currentScore+currentScoreLines;
        }
    }
}

