void Initializer::Triangulate(const cv::KeyPoint &kp1, const cv::KeyPoint &kp2, const cv::Mat &P1, const cv::Mat &P2, cv::Mat &x3D)
{
    cv::Mat A(4,4,CV_32F);

    A.row(0) = kp1.pt.x*P1.row(2)-P1.row(0);
    A.row(1) = kp1.pt.y*P1.row(2)-P1.row(1);
    A.row(2) = kp2.pt.x*P2.row(2)-P2.row(0);
    A.row(3) = kp2.pt.y*P2.row(2)-P2.row(1);

    cv::Mat u,w,vt;
    cv::SVD::compute(A,w,u,vt,cv::SVD::MODIFY_A| cv::SVD::FULL_UV);
    x3D = vt.row(3).t();
    x3D = x3D.rowRange(0,3)/x3D.at<float>(3);
}

//三角测量恢复地图线首尾端点
void Initializer::TriangulateLine(const KeyLine &kL1, const KeyLine &kL2, const cv::Mat &P1, const cv::Mat &P2, cv::Mat &xFirP3D, cv::Mat &xEndP3D)
{
    //地图线首端点
    cv::Mat A(4,4,CV_32F);

    A.row(0) = kL1.startPointX*P1.row(2)-P1.row(0);
    A.row(1) = kL1.startPointY*P1.row(2)-P1.row(1);
    A.row(2) = kL2.startPointX*P2.row(2)-P2.row(0);
    A.row(3) = kL2.startPointY*P2.row(2)-P2.row(1);

    cv::Mat u,w,vt;
    cv::SVD::compute(A,w,u,vt,cv::SVD::MODIFY_A| cv::SVD::FULL_UV);
    xFirP3D = vt.row(3).t();
    xFirP3D= xFirP3D.rowRange(0,3)/xFirP3D.at<float>(3);
    
    //地图线尾端点
    cv::Mat A1(4,4,CV_32F);

    A1.row(0) = kL1.endPointX*P1.row(2)-P1.row(0);
    A1.row(1) = kL1.endPointY*P1.row(2)-P1.row(1);
    A1.row(2) = kL2.endPointX*P2.row(2)-P2.row(0);
    A1.row(3) = kL2.endPointY*P2.row(2)-P2.row(1);

    cv::Mat u1,w1,vt1;
    cv::SVD::compute(A1,w1,u1,vt1,cv::SVD::MODIFY_A| cv::SVD::FULL_UV);
    xEndP3D = vt1.row(3).t();
    xEndP3D= xEndP3D.rowRange(0,3)/xEndP3D.at<float>(3);
    
}

void Initializer::Normalize(const vector<cv::KeyPoint> &vKeys, vector<cv::Point2f> &vNormalizedPoints, cv::Mat &T)
{
    float meanX = 0;
    float meanY = 0;
    const int N = vKeys.size();

    vNormalizedPoints.resize(N);

    for(int i=0; i<N; i++)
    {
        meanX += vKeys[i].pt.x;
        meanY += vKeys[i].pt.y;
    }

    meanX = meanX/N;
    meanY = meanY/N;

    float meanDevX = 0;
    float meanDevY = 0;

    for(int i=0; i<N; i++)
    {
        vNormalizedPoints[i].x = vKeys[i].pt.x - meanX;
        vNormalizedPoints[i].y = vKeys[i].pt.y - meanY;

        meanDevX += fabs(vNormalizedPoints[i].x);
        meanDevY += fabs(vNormalizedPoints[i].y);
    }

    meanDevX = meanDevX/N;
    meanDevY = meanDevY/N;

    float sX = 1.0/meanDevX;
    float sY = 1.0/meanDevY;

    for(int i=0; i<N; i++)
    {
        vNormalizedPoints[i].x = vNormalizedPoints[i].x * sX;
        vNormalizedPoints[i].y = vNormalizedPoints[i].y * sY;
    }

    T = cv::Mat::eye(3,3,CV_32F);
    T.at<float>(0,0) = sX;
    T.at<float>(1,1) = sY;
    T.at<float>(0,2) = -meanX*sX;
    T.at<float>(1,2) = -meanY*sY;
}

